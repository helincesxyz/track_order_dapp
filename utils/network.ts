export const switchNetwork = async (networkName: keyof typeof networks) => {
  if (!window.ethereum) throw new Error('No crypto wallet found');
  
  const provider = new ethers.BrowserProvider(window.ethereum);
  const network = await provider.getNetwork();
  
  const desiredNetwork = networks[networkName];
  
  if (network.chainId !== BigInt(`0x${desiredNetwork.chainId}`)) {
    try {
      await window.ethereum.request({
        method: 'wallet_switchEthereumChain',
        params: [{ chainId: desiredNetwork.chainId }],
      });
    } catch (switchError: any) {
      if (switchError.code === 4902) {
        await window.ethereum.request({
          method: 'wallet_addEthereumChain',
          params: [desiredNetwork],
        });
      }
    }
  }
};
